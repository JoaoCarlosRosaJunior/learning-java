package EstruturasRepetitivas.For;

public class Main {
    public static void main(String[] args) {
        // Utilizamos o for quando sabemos exatamente a quantidade de repetições
        // necessárias.

        // Exemplo simples de contaador 0-10 para entender a sintaxe
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
